#ifndef __MINT_PRIVATE_H__
#define __MINT_PRIVATE_H__

/**********************************************************************************************************************
 *  GLOBAL CONSTANT MACROS
 *********************************************************************************************************************/

#define CORTEXM4_REGISTERS_BASE_ADDR                (0xE000E000)
#define PRIn_Base                                   (CORTEXM4_REGISTERS_BASE_ADDR + 0x400)                                                                                      
#define EN_Base                                     (CORTEXM4_REGISTERS_BASE_ADDR + 0x100)     


/**********************************************************************************************************************
 *  GLOBAL REGISTERS
 *********************************************************************************************************************/

/*Application Interrupt and Reset Control (APINT)*/
#define APINT                                   *((volatile u32_t*)(CORTEXM4_REGISTERS_BASE_ADDR + 0xD0C))

/* Interrupt Priority (PRIx)*/
#define PRI(n)                                  *((volatile u32_t*)(PRIn_Base + (4 * n)))   

/* Interrupt Set Enable (ENx)*/
#define EN(n)                                   *((volatile u32_t*)(EN_Base + (4 * n)))     



#endif  /* __MINT_PRIVATE_H__ */

